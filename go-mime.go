// TypeByExtension returns the content type based on the extension of the file
func TypeByExtension(extension string) string {
	mimeTypeInfo := TypeInfoByExtension(extension)
	if mimeTypeInfo == nil {
		return ""
	}
	return mimeTypeInfo.ContentType
}

// TypeInfoByExtension returns the MIMETypeInfo based on the extension of the file
func TypeInfoByExtension(extension string) *MIMETypeInfo {
	if strings.HasPrefix(extension, ".") {
		// Remove the period
		extension = extension[1:]
	}
	mimeTypeInfo, has := mimeTypes[strings.ToLower(extension)]
	if has {
		return &mimeTypeInfo
	}
	return nil
}
